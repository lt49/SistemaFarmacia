public class alumno {
    
    int codigoUniv;
    String escuelaAcademica;
    String facultad;
    String  salon;
    
    
    
    public alumno() {
        
    }

    public int getCodigoUniv() {
        return codigoUniv;
    }

    public void setCodigoUniv(int codigoUniv) {
        this.codigoUniv = codigoUniv;
    }

    public String getEscuelaAcademica() {
        return escuelaAcademica;
    }

    public void setEscuelaAcademica(String escuelaAcademica) {
        this.escuelaAcademica = escuelaAcademica;
    }

    public String getFacultad() {
        return facultad;
    }

    public void setFacultad(String facultad) {
        this.facultad = facultad;
    }

    public String getSalon() {
        return salon;
    }

    public void setSalon(String salon) {
        this.salon = salon;
    }

    
}
